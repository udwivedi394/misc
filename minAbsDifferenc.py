INF = 10**10

def minabsdifference(A,B,C):
    ctr1 = ctr2 = ctr3 = 0
    a,b,c = len(A),len(B),len(C)
    minabs = INF
    while ctr1 < a and ctr2 < b and ctr3 < c:
        maxi = max(A[min(a-1,ctr1)],B[min(b-1,ctr2)],C[min(c-1,ctr3)])
        mini = min(A[min(a-1,ctr1)],B[min(b-1,ctr2)],C[min(c-1,ctr3)])
        #print A[min(a-1,ctr1)],B[min(b-1,ctr2)],C[min(c-1,ctr3)],abs(maxi-mini)
       
        if minabs > abs(maxi-mini):
            minabs = abs(maxi-mini)
       
        if mini == A[min(a-1,ctr1)]:
            ctr1 += 1
        elif mini == B[min(b-1,ctr2)]:
            ctr2 += 1
        elif mini == C[min(c-1,ctr3)]:
            ctr3 += 1
        else:
            ctr1 += 1
            ctr2 += 1
            ctr3 += 1

    return minabs


